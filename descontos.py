def desconto_10(preco):
    """Desconto de 10%

    :param preco: preço do produto
    :return: preço com desconto de 10%
    """
    return 0.9 * preco


def desconto_50(preco):
    """Desconto de 50%

    :param preco: preço do produto
    :return: preço com desconto de 50%
    """
    return 0.50 * preco
