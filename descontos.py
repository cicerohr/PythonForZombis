def desconto_10(preco: float) -> float:
    """Desconto de 10%

    :param preco: preço do produto
    :type preco: float
    :return: preço com desconto de 10%
    :rtype: float
    """
    return 0.9 * preco


def desconto_50(preco: float) -> float:
    """Desconto de 50%

    :param preco: preço do produto
    :type preco: float
    :return: preço com desconto de 50%
    :rtype: float
    """
    return 0.50 * preco
